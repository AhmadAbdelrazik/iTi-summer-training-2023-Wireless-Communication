#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char 		uint8;
typedef unsigned int 		uint16;
typedef unsigned long int 	uint32;
typedef unsigned long long 	uint64;

typedef char 		sint8;
typedef short 		sint16;
typedef int 		sint32;
typedef long 		sint64;

typedef float f32;
typedef double d64;


#endif /* STD_TYPES_H_ */
